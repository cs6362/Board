DROP TABLE REPLY;
DROP TABLE BOARD;

CREATE TABLE BOARD
(
	BOARDNO VARCHAR2(20) PRIMARY KEY,
	BOARDTITLE VARCHAR2(20),
	BOARDWRITER VARCHAR2(20),
	BOARDCONTENT VARCHAR2(2000),
	BOARDDATE DATE
);

CREATE TABLE REPLY
(
	RENO NUMBER PRIMARY KEY,
	/* MID VARCHAR2(2000) REFERENCES MEMBER (MID), */
	RECONTENT VARCHAR2(2000) NOT NULL,
	BNO VARCHAR2(20) REFERENCES BOARD (BOARDNO),
	RECATEGORY NUMBER NOT NULL,
	RENOTICEDATE DATE
);


